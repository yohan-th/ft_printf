#include "libft.h"

/*
** Recherche le premiere occurence de c dans s dans la limite de n octet
** et renvoi *s point√© sur c
*/

void	*ft_memchr(const void *s, int c, size_t n)
{
	while (n--)
	{
		if (*(unsigned char *)s == (unsigned char)c)
			return ((void *)s);
		s++;
	}
	return (NULL);
}
