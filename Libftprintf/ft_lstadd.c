#include "libft.h"

/*
** Ajoute l’élément new en tête de la liste.
**
** Param :
** 1. L’adresse d’un pointeur sur le premier maillon d’une liste.
** 2. Le maillon à ajouter en tête de cette liste.
*/

void	ft_lstadd(t_list **alst, t_list *new)
{
	if (alst != NULL && new != NULL)
	{
		new->next = *alst;
		*alst = new;
	}
}
