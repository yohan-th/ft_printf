#include "libft.h"

/*
** Recherche l'element {c} et retourne {s} point√© sur {c} ou NULL si non trouve
*/

char		*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = ft_strlen(s);
	while (i != 0 && s[i] != (char)c)
		i--;
	if (s[i] == (char)c)
		return ((char *)&s[i]);
	return (0);
}
