#include "libft.h"

/*
** Parcourt la liste lst en appliquant Ã  chaque maillon la fonction f.
*/

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	while (lst)
	{
		f(lst);
		lst = lst->next;
	}
}
